import UIKit

extension UIColor {
    
    public class var accentPrimary: UIColor {
        return UIColor(named: "accentPrimary")!
    }

    public class var accentSecondary: UIColor {
        return UIColor(named: "accentSecondary")!
    }
    
    public class var background: UIColor {
        return UIColor(named: "background")!
    }
    
    public class var backgroundLight: UIColor {
       return UIColor(named: "backgroundLight")!
    }
    
    public class var backgroundDark: UIColor {
       return UIColor(named: "backgroundDark")!
    }
    
    public class var textPrimary: UIColor {
        return UIColor(named: "textPrimary")!
    }
    
    public class var textSecondary: UIColor {
        return UIColor(named: "textSecondary")!
    }
}
